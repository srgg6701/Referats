        работ и в своей жизни написал только одну дипломную работу. За 3 дня
        :) Причём, работа была заказная и получила оценку &laquo;отлично&raquo;.
        Если вам любопытно взглянуть, то она <a href="http://referats.info/content.php?wnum=821">здесь</a>. Но: </p>
      <ul>
        <li>тема работы в точности
          соответствовала профилю моей деятельности и я прекрасно ориентировался
          в этом предмете</li>
        <li>у меня были некоторые исходные материалы</li>
        <li>у меня профессиональные навыки поиска информации в интернет</li>
        <li>в конце концов &#8212; у меня масса коллег, способных и готовых оказать
