<p>Первая реакция на подобного рода проблему у любого человека &#8212; это
        воскликнуть: &laquo;Да как же вы до этого докатились (дожили, допустили
        и т.п.)?!!!&raquo;. Но, поскольку ничего конструктивного в этом возгласе
        нет, давайте не будем разбираться, &laquo;кто виноват&raquo;, а сосредоточимся
        на втором извечном вопросе &#8212; &laquo;что делать?&raquo;</p>
      <div class="paddingTop10 paddingBottom6 txt120"><b>Итак, что же нам теперь делать?</b></div>
      <p>Давайте быть реалистами &#8212; чудес не бывает. Невозможно написать
        диплом за 3 дня, НИЧЕГО не зная о его предмете и не располагая хоть каким-нибудь
        исходным материалом. А возможно ли это вообще? Мой ответ однозначен &#8212; да,
        возможно. Но &#8212; не всегда. Как обычно, всё зависит
        от множества обстоятельств.</p>
