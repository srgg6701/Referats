      <p>Перед тем, как героически &laquo;бросаться грудью на амбразуру&raquo;,
        честно спросите себя &#8212; а есть ли всё это у вас? Если есть, что
        ж, шансы сделать это самостоятельно имеют место быть. Если какой-то из
        компонентов (тем более &#8212; несколько) отсутствуют, тогда я бы вам
        посоветовал следующее:</p>
      <ol>
