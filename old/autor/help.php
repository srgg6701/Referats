<p><a href="#exit">Выход</a> &#8212; закончив работу со своим аккаунтом, не забудьте &laquo;закрыть за собой дверь&raquo; и щёлкнуть по этой ссылке, иначе в ваш аккаунт может проникнуть кто-нибудь другой... </p>
<p><a href="#customer_account">Эккаунт заказчика</a> &#8212; если вы зарегистрированы не только, как автор, но и как заказчик (т.е., заказали у кого-либо из других авторов работу), вы сможете легко переключаться между своим аккаунтом автора и заказчика. Это осуществляется щелчком по пиктограмме <img src="../../images/switch.gif" width="54" height="48" hspace="6" align="texttop">. Если же вы не являетесь заказчиком, вы её просто не увидите. </p>

