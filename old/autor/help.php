<h3><hr size="1" noshade><a name="getting_started"></a>1. Приступая к работе</h3>
<br>
Ваш аккаунт &#8212; это персональный раздел, позволяющий вам контролировать свою
  деятельность в качестве автора на торговой площадке Referats.info.
<p>Используя его функции, вы можете: </p>
<ul>
  <li>Видеть свои работы, выставленные на продажу </li>
  <li>Видеть заявки на ваши работы, как потенциальных покупателей, так и тех, кто
    уже заплатил за них деньги</li>
  <li>Изменять параметры своих работ</li>
  <li>Отсылать и пересылать сообщения администрации проекта и заказчикам</li>
  <li>Видеть, сколько денег мы должны выплатить вам за проданные работы и т.д. </li>
</ul>
<p>Наша система практически полностью автоматизирована. Все процедуры с вашими
  работами, такие, как изменение их статуса, проплаты заказчиков, новые сообщения
  по заказам и т.п. отслеживаются, и наша система извещает вас об этом по указанному
  вами при регистрации емэйлу. </p>
<p>Функции вашего аккаунта достаточно стандартны для подобного рода систем и
  не должны вызвать у вас затруднений в работе, если вы уже имеете некоторый
  опыт общения с ПК.</p>
<p>Для полноценной работы мы рекомендуем установить вам разрешение экрана не
  менее 1024Х768. Для того, чтобы узнать, какое текущее разрешение экрана монитора
  вашего компьютера, щёлкните <a href="javascript:alert('Текущее разрешение экрана вашего монитора &#8212; '+screen.width+'X'+screen.height);">здесь</a>. </p>
