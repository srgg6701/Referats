          <h3>
            <hr size="1" noshade>
            <a name="sent_worx"></a>6. Отправленные работы </h3>
  <br>
  Сразу после того, как заказчик полностью оплатил заказанную работу, вы получаете по емэйлу его контактные данные (адрес электронной почты), на которые нужно отослать ему готовую работу. 
  <p>Как только вы подтвердите отправку из своего аккаунта, статус этого заказа меняется и становится &laquo;Отправленной работой&raquo;. Это означает, что вы сообщили нам о её отправке, однако мы пока что не получали от заказчика подтверждения о её получении. Если в течении некоторого срока (1-3 дня) он не известит нас об этом, наша система сама спросит его. Если же ответа снова не будет, мы будем считать, что работа получена и заказчика устраивает. В данном случае мы выплачиваем вам деньги. При этом заказ автоматически изменяет статус на &laquo;Закрытый&raquo; (&laquo;Архивный&raquo;) и перемещается в соответствующий раздел. </p>
  <p>Внешний вид раздела &laquo;Отправленные работы&raquo; отличается от предыдущего только тем, что содержит в себе один фрейм, а не два. </p>
  <h3>
    <hr size="1" noshade>
    <a name="debts"></a>7. Задолженности </h3>
  <br> 
