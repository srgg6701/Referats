  В каждом фрейме загружается своя, отдельная страница. Содержание левого фрейма
  никогда не изменяется &#8212; он содержит в себе навигационное меню:</p>
<p><img src="../illustrations/menu.gif" width="428" height="495" border="1"> </p>
