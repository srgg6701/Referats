<h3><hr size="1" noshade><a name="annotation"></a>2. Аннотация разделов</h3>
<br> 
  Предназначение разделов вашего аккаунта следующее:
  <p><a href="#add_work">Добавить работу</a> &#8212; размещение тем работ, выставляемых вами на продажу в БД (базе данных) нашей торговой площадки.</p>
<p><a href="#worx">Выставленные работы</a> &#8212; список работ, выставленных вами на продажу. Могут быть двух типов (статусов): утверждённые администрацией проекта и неутверждённые, т.е., находящиеся на рассмотрении. </p>
<p>В первом случае заказчики не видят ваших работ в каталоге и не могут найти их через функцию поиска на сайте, т.е., они для них, фактически, не существуют. </p>
<p>Во втором случае они становятся доступными для  заявок потенциальных покупателей.</p>
<p><a href="#applications" class="red">!Заявки заказчиков</a> &#8212; оплаченные и неоплаченные (или оплаченные не полностью) заявки потенциальных и реальных заказчиков на ваши работы.</p>
<p><a href="#sent_worx">Отправленные работы</a> &#8212; те работы, которые вы уже отправили заказчикам и известили об этом нас. Процедура оповещения администрации торговой площадки об отправке работ заказчику также выполняется централизовано, из своего аккаунта (подробности вы узнаете ниже).</p>
<p><a href="#debts">Задолженности</a> &#8212; список работ, за которые мы с вами ещё не расплатились или расплатились не полностью. </p>
<p><a href="#closed_worx">Закрытые заказы</a> &#8212; те заказы, по которым мы с вами полностью расплатились. </p>
<p><a href="#messages">Сообщения</a> &#8212; раздел, посвящённый статистике сообщений с нами и с заказчиками.</p>
<p><a href="#personal_data">Регистрационные данные</a> &#8212; ваши личные данные, указанные вами при регистрации. Вы можете изменять все их, кроме поля &laquo;Как к вам обращаться&raquo;.</p>
<p><a href="#faq">FAQ</a> &#8212; ответы на наиболее часто встречающиеся вопросы по работе в нашей системе.</p>
<p><a href="#contacts">Контакты</a> &#8212; если уже ничего не помогает, и последний шанс получить ответ на вопрос &#8212; прямо спросить у нас, загляните в этот раздел и позвоните по указанному там телефону. :) Или свяжитесь по icq.</p>
<p><a href="#exit">Выход</a> &#8212; закончив работу со своим аккаунтом, не збудьте &laquo;закрыть за собой дверь&raquo; и щёлкнуть по этой ссылке, иначе в ваш аккаунт может проникнуть кто-нибудь другой... </p>
<p><a href="#customer_account">Эккаунт заказчика</a> &#8212; если вы зарегистрированы не только, как автор, но и как заказчик (т.е., заказали у кого-либо из других авторов работу), вы сможете легко переключаться между своим аккаунтом автора и заказчика. Это осуществляется щелчком по пиктограмме <img src="file:///D|/AppServ/www/images/switch.gif" width="54" height="48" hspace="6" align="texttop">. Если же вы не являетесь заказчиком, вы её просто не увидите. </p>

