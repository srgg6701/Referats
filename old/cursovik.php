        <h1 class="header6bottom"><b><img src="../images/i_gradient.gif" width="22" height="22" align="absmiddle" style="margin-right:6px">Курсовая на отлично – это легко!</b></h1>
      <span class="header6bottom"></span> <b><img name="Portrait" src="../images/writers/course2.jpg" width="116" height="145" alt="" hspace="22" vspace="2" align="left"></b> <em>штат
      отличников набирает Жанна
      Стрельцова</em>      <hr size="1" noshade color="#FF9900">
      <p style="padding-top:0px; margin-top:10px">Да-да, Курсовая на отлично – это легко, и никак иначе. А для этого совсем необязательно быть вундеркиндом или ночевать в библиотеке, пытаясь найти материал для экзотической темы (а куда вы смотрели, когда тему выбирали?). </p>
      <p>Такие страдания — не для нас. Достаточно изучить несколько простых правил, и написание-сдача курсовой не будет для вас болезненным процессом. Итак, шаг за шагом…</p>
      <h1>Выбираем тему курсовой работы</h1>
      <p>В общем-то, на кафедре всегда есть список тем, из которых можно выбрать понравившуюся. Правда, список этот — ориентировочный, и вы всегда можете предложить свой вариант. Так или иначе, следует придерживаться простого правила при выборе темы курсовой: тема не должна быть слишком узкой (вы же не реферат пишете, а настоящую исследовательскую работу!). Но и слишком широкая, глобальная тема вряд ли подойдет — на 30-50 страницах особо не разбежишься, и до конца раскрыть широкую тему в курсовой работе вряд ли получится. Поставят низкую оценку, как пить дать! </p>
      <p>Выбирайте тему из области, в которой хорошо разбираетесь и хотя бы примерно представляете, о чем будете писать и где будете искать материал. </p>
      <p>И еще обратите внимание, у кого будете писать курсовую. Вам ведь придется общаться с этим преподавателем как минимум трижды.</p>
      <h1>Контакт с научным руководителем</h1>
      <p>Слышали, наверное, страшилки о том, как препод заставлял студента переписывать работу по 3-4 раза? Это, конечно, не каждый день случается, но бывает всякое. И основная причина даже не в том, что все преподаватели вредные и капризные, или все студенты пишут чушь в своих работах. </p>
      <p>Просто нужно вовремя появиться у преподавателя с предварительным вариантом работы (если в первый раз появитесь у руководителя за 2-3 дня до сдачи — пеняйте на себя!). </p>
      <p>Заставит что-то переделывать — не спешите кардинально менять всю работу. Может случиться так, что вы все-таки вернетесь к первоначальному варианту. Главное — внимательно выслушивать замечания преподавателя, уметь отстаивать свою точку зрения, где надо, и соглашаться, если где-то действительно нужно сделать изменения.</p>
      <p>На самом деле иногда у руководителя просто нет времени вникнуть в суть вопроса, и он по инерции отсылает студента переделывать, править — ведь с первого раза, мол, идеально получиться не может.</p>
      <h1>Готовимся к защите курсовой работы</h1>
      <p>По большому счету, вашу работу от корки до корки никто читать не будет. Главное — безупречное введение и выводы в конце работы. Вот это читают особо внимательно, поэтому, даже если саму работу писали не вы (скажем, вы скачали готовую работу в интернете или вам написали ее на заказ), введение и выводы постарайтесь написать сами. Это — ваша визитная карточка, так сказать.</p>
      <p>За день до защиты внимательно перечитайте работу, чтобы основной смысл остался в памяти, и вы могли легко ответить на дополнительные вопросы.</p>
      <p>Во время защиты ведите себя уверенно. Если вам задали какой-то вопрос, на который вы не знаете точного ответа — не тушуйтесь. Невозмутимо отвечайте, что вопрос интересный, но, к сожалению, в рамки курсовой не вошел. Но вы обязательно изучите его детальнее при написании дипломной работы. Главное — не молчите.</p>
